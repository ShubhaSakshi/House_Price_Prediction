# Import libraries# Import libraries
import streamlit as st
import joblib


st.set_page_config(
    page_title="House Price Prediction",
    page_icon="🏠",
    layout="wide"
)


# Load model and scaler

model = joblib.load("model.pkl")
scaler = joblib.load("scaler.pkl")

# Title
st.title("🏠 House Price Prediction App")

st.sidebar.title("🏠 House Price Prediction")

st.sidebar.markdown("""
### About
This application predicts California house prices using a Machine Learning model.

### Model
- Random Forest Regressor

### Developed By
Shubha Sakshi
""")

# Description
st.write("Enter house details and predict the house price using Random Forest.")

col1, col2 = st.columns(2)

with col1:
    medinc = st.number_input("Median Income")
    houseage = st.number_input("House Age")
    averooms = st.number_input("Average Rooms")
    avebedrms = st.number_input("Average Bedrooms")

with col2:
    population = st.number_input("Population")
    aveoccup = st.number_input("Average Occupancy")
    latitude = st.number_input("Latitude")
    longitude = st.number_input("Longitude")


# Predict button

if st.button("Predict Price"):

    # Create input data
    input_data = [[
        medinc,
        houseage,
        averooms,
        avebedrms,
        population,
        aveoccup,
        latitude,
        longitude
    ]]

    # Scale input
    input_data_scaled = scaler.transform(input_data)


    prediction = model.predict(input_data_scaled)

    predicted_price = prediction[0] * 100000

    st.success(f"🏠 Predicted House Price: ${predicted_price:,.2f}")

    st.info(
       "💡 This is the estimated house price generated by the trained Random Forest model "
       "based on the values you entered."
         )

    st.markdown("---")

    st.subheader("📋 Prediction Summary")

    st.write(f"**Estimated Price:** ${predicted_price:,.2f}")
    st.write("**Model Used:** Random Forest Regressor")
    st.write("**Dataset:** California Housing Dataset")

    
    


st.markdown("---")
st.write("Built with Python, Scikit-Learn, Random Forest and Streamlit")